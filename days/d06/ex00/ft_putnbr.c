void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	if (nb == 2147483647)
	{
		ft_putchar('2');
		nb = 147483647;
	}
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		nb = 147483648;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + '0');
}
