char *ft_strcat(char *dest, char *src)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (src[i] != '\0')
		i++;
	i++;
	while (j < i && dest[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	return (dest);
}
