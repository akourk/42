char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (*(src + i + 1) != '\0')
	{
		*(dest + i) = *(src + i);
		i++;
	}
	*(dest + (i + 1)) = '\0';
	return (dest);
}
